import React from "react";
import { useHrAttendanceData } from "@/hooks/useHrData";
import { SectionCard } from "@/components/Section";
import { Button, StatusMessage } from "@/components/ui";
import { StatusTag } from "@/components/HR/MetricCard";
import HrAttendanceUi from "@/components/page-ui/hr/hr-attendance-ui"; // Extracted presentation layer.

export default function HrAttendance() {
  const { data, isLoading, error } = useHrAttendanceData();

  const attendanceLogs = data?.attendanceLogs ?? [];
  const overtimeSummaries = data?.overtimeSummaries ?? [];
  const absenceRecords = data?.absenceRecords ?? [];

  return <HrAttendanceUi view="section1" absenceRecords={absenceRecords} attendanceLogs={attendanceLogs} Button={Button} error={error} isLoading={isLoading} overtimeSummaries={overtimeSummaries} SectionCard={SectionCard} StatusMessage={StatusMessage} StatusTag={StatusTag} />;















































































































































































}
