const fs = require("fs");
const path = require("path");

const ROOT = process.cwd();
const SRC_DIR = path.join(ROOT, "src");
const OUT_FILE = path.join(SRC_DIR, "lib", "dev-layout", "sectionSourceMap.generated.js");

const EXTENSIONS = new Set([".js", ".jsx", ".ts", ".tsx"]);
const IGNORE_DIRS = new Set(["node_modules", ".next", ".git"]);

const toPosix = (value) => value.split(path.sep).join("/");

const escapeForJs = (value) =>
  String(value || "")
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");

const walk = (dir, files = []) => {
  fs.readdirSync(dir, { withFileTypes: true }).forEach((entry) => {
    if (IGNORE_DIRS.has(entry.name)) return;
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath, files);
      return;
    }
    if (EXTENSIONS.has(path.extname(entry.name))) {
      files.push(fullPath);
    }
  });
  return files;
};

const getLineNumber = (text, index) => text.slice(0, index).split(/\r?\n/).length;

const normalizeTemplatePattern = (value) =>
  String(value || "")
    .replace(/\$\{[^}]+\}/g, "*")
    .replace(/`/g, "")
    .trim();

const collectMatches = (filePath) => {
  const text = fs.readFileSync(filePath, "utf8");
  const relPath = toPosix(path.relative(ROOT, filePath));
  const matches = [];
  const seen = new Set();
  const patterns = [
    {
      prop: "sectionKey",
      regex: /sectionKey\s*=\s*(?:"([^"]+)"|'([^']+)'|`([^`]+)`|\{`([^`]+)`\}|\{"([^"]+)"\}|\{'([^']+)'\})/g,
    },
    {
      prop: "data-dev-section-key",
      regex: /data-dev-section-key\s*=\s*(?:"([^"]+)"|'([^']+)'|`([^`]+)`|\{`([^`]+)`\}|\{"([^"]+)"\}|\{'([^']+)'\})/g,
    },
  ];

  patterns.forEach(({ prop, regex }) => {
    let match;
    while ((match = regex.exec(text))) {
      const rawValue = match.slice(1).find((value) => value != null) || "";
      const pattern = normalizeTemplatePattern(rawValue);
      if (!pattern) continue;
      const line = getLineNumber(text, match.index);
      const dedupeKey = `${pattern}:${relPath}:${line}:${prop}`;
      if (seen.has(dedupeKey)) continue;
      seen.add(dedupeKey);
      matches.push({
        key: pattern,
        file: relPath,
        line,
        prop,
        dynamic: pattern.includes("*"),
      });
    }
  });

  return matches;
};

const entries = walk(SRC_DIR)
  .flatMap(collectMatches)
  .sort((left, right) => {
    if (left.key !== right.key) return left.key.localeCompare(right.key);
    if (left.file !== right.file) return left.file.localeCompare(right.file);
    return left.line - right.line;
  });

const output = `// Generated by tools/scripts/generate-dev-layout-section-source-map.js.
// Do not edit by hand; rerun the generator after adding or moving dev layout section keys.
export const DEV_LAYOUT_SECTION_SOURCE_MAP = [
${entries
  .map(
    (entry) =>
      `  { key: \`${escapeForJs(entry.key)}\`, file: \`${escapeForJs(entry.file)}\`, line: ${entry.line}, prop: \`${entry.prop}\`, dynamic: ${entry.dynamic} },`
  )
  .join("\n")}
];
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, output, "utf8");

console.log(`Wrote ${entries.length} dev layout section source entries to ${toPosix(path.relative(ROOT, OUT_FILE))}`);
